const seedCount = Number(process.env.SEED_PRODUCTS_COUNT ?? '500');
const catalogUrlFromEnv = process.env.CATALOG_URL;
const warehouseUrlFromEnv = process.env.WAREHOUSE_URL;
const gatewayUrl = process.env.GATEWAY_URL ?? 'http://localhost:8080';

const seedBrands = [
  { name: 'Nordhale', slug: 'nordhale', description: 'Tech accessories premium' },
  { name: 'Urban Pulse', slug: 'urban-pulse', description: 'Style and street essentials' },
  { name: 'Motion Lab', slug: 'motion-lab', description: 'Performance and sport gear' },
];

const seedCategories = [
  { name: 'Elettronica', slug: 'elettronica', description: 'Device e accessori' },
  { name: 'Moda', slug: 'moda', description: 'Abbigliamento e lifestyle' },
  { name: 'Sport', slug: 'sport', description: 'Training e outdoor' },
  { name: 'Casa', slug: 'casa', description: 'Home essentials' },
];

const seedCollections = [
  { name: 'New Season', slug: 'new-season', description: 'Nuova stagione', isFeatured: true },
  { name: 'Best Deals', slug: 'best-deals', description: 'Promo e sconti', isFeatured: true },
  { name: 'Editor Picks', slug: 'editor-picks', description: 'Scelti dal team', isFeatured: false },
];
const emptyGuid = '00000000-0000-0000-0000-000000000000';

function randomInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function randomSku(index) {
  return `SKU-${Date.now().toString(36).toUpperCase()}-${index}-${randomInt(1000, 9999)}`;
}

function isValidCatalogRef(entity) {
  if (!entity || typeof entity !== 'object') {
    return false;
  }

  const id = String(entity.id ?? '').trim().toLowerCase();
  const slug = String(entity.slug ?? '').trim();
  return id.length > 0 && id !== emptyGuid && slug.length > 0;
}

function asArray(value) {
  return Array.isArray(value) ? value : [];
}

function randomProduct(index, brands, categories, collections) {
  const adjectives = ['Fast', 'Smart', 'Ultra', 'Compact', 'Pro', 'Air', 'Prime', 'Edge'];
  const nouns = ['Keyboard', 'Mouse', 'Headset', 'Monitor', 'Webcam', 'Dock', 'Microphone', 'Speaker'];
  const adjective = adjectives[randomInt(0, adjectives.length - 1)];
  const noun = nouns[randomInt(0, nouns.length - 1)];
  const name = `${adjective} ${noun}`;
  const brand = brands[randomInt(0, brands.length - 1)];
  const category = categories[randomInt(0, categories.length - 1)];

  const collectionPool = [...collections].sort(() => Math.random() - 0.5).slice(0, randomInt(1, 2));

  return {
    sku: randomSku(index),
    name,
    description: `${name} generated by seed script`,
    price: Number((randomInt(1500, 25999) / 100).toFixed(2)),
    brandId: brand.id,
    categoryId: category.id,
    collectionIds: collectionPool.map((x) => x.id),
    isNewArrival: index <= Math.ceil(seedCount * 0.3),
    isBestSeller: index % 3 === 0,
  };
}

async function waitFor(url, retries = 60, delayMs = 2000) {
  for (let attempt = 1; attempt <= retries; attempt += 1) {
    try {
      const response = await fetch(url);
      if (response.ok) {
        return;
      }
    } catch {
      // retry
    }

    await new Promise((resolve) => setTimeout(resolve, delayMs));
  }

  throw new Error(`Service not ready: ${url}`);
}

async function resolveCatalogServiceUrl() {
  if (catalogUrlFromEnv) {
    return catalogUrlFromEnv.replace(/\/$/, '');
  }

  const candidates = [
    'http://catalog-api:8080',
    `${gatewayUrl.replace(/\/$/, '')}/api/catalog`
  ];

  for (const candidate of candidates) {
    try {
      await waitFor(`${candidate}/health/ready`, 3, 1000);
      return candidate;
    } catch {
      // try next candidate
    }
  }

  return candidates[1];
}

async function resolveWarehouseServiceUrl() {
  if (warehouseUrlFromEnv) {
    return warehouseUrlFromEnv.replace(/\/$/, '');
  }

  const candidates = [
    'http://warehouse-api:8080',
    `${gatewayUrl.replace(/\/$/, '')}/api/warehouse`
  ];

  for (const candidate of candidates) {
    try {
      await waitFor(`${candidate}/health/ready`, 3, 1000);
      return candidate;
    } catch {
      // try next candidate
    }
  }

  return candidates[1];
}

async function postJson(url, body) {
  const response = await fetch(url, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(body)
  });

  if (!response.ok) {
    const text = await response.text();
    throw new Error(`POST ${url} failed (${response.status}): ${text}`);
  }

  return response.json();
}

async function postJsonWithRetry(url, body, options = {}) {
  const retries = options.retries ?? 5;
  const delayMs = options.delayMs ?? 250;
  const shouldRetry = options.shouldRetry ?? (() => false);

  for (let attempt = 1; attempt <= retries; attempt += 1) {
    try {
      return await postJson(url, body);
    } catch (error) {
      const message = error instanceof Error ? error.message : String(error);
      if (attempt >= retries || !shouldRetry(message)) {
        throw error;
      }

      await new Promise((resolve) => setTimeout(resolve, delayMs));
    }
  }

  throw new Error(`POST ${url} failed after ${retries} attempts`);
}

async function getJson(url) {
  const response = await fetch(url);
  if (!response.ok) {
    const text = await response.text();
    throw new Error(`GET ${url} failed (${response.status}): ${text}`);
  }

  return response.json();
}

async function main() {
  const catalogServiceUrl = await resolveCatalogServiceUrl();
  const warehouseServiceUrl = await resolveWarehouseServiceUrl();

  console.log(`[seed] catalog base url: ${catalogServiceUrl}`);
  console.log(`[seed] warehouse base url: ${warehouseServiceUrl}`);
  console.log('[seed] waiting for catalog service');
  await waitFor(`${catalogServiceUrl}/health/ready`);
  await waitFor(`${warehouseServiceUrl}/health/ready`);

  console.log('[seed] creating brands/categories/collections (idempotent)');
  const existingBrands = asArray(await getJson(`${catalogServiceUrl}/v1/brands`)).filter(isValidCatalogRef);
  const existingCategories = asArray(await getJson(`${catalogServiceUrl}/v1/categories`)).filter(isValidCatalogRef);
  const existingCollections = asArray(await getJson(`${catalogServiceUrl}/v1/collections`)).filter(isValidCatalogRef);

  const existingBrandSlugs = new Set((Array.isArray(existingBrands) ? existingBrands : []).map((x) => x.slug));
  const existingCategorySlugs = new Set((Array.isArray(existingCategories) ? existingCategories : []).map((x) => x.slug));
  const existingCollectionSlugs = new Set((Array.isArray(existingCollections) ? existingCollections : []).map((x) => x.slug));

  for (const brand of seedBrands) {
    if (!existingBrandSlugs.has(brand.slug)) {
      await postJson(`${catalogServiceUrl}/v1/brands`, brand);
    }
  }

  for (const category of seedCategories) {
    if (!existingCategorySlugs.has(category.slug)) {
      await postJson(`${catalogServiceUrl}/v1/categories`, category);
    }
  }

  for (const collection of seedCollections) {
    if (!existingCollectionSlugs.has(collection.slug)) {
      await postJson(`${catalogServiceUrl}/v1/collections`, collection);
    }
  }

  let brands = asArray(await getJson(`${catalogServiceUrl}/v1/brands`)).filter(isValidCatalogRef);
  let categories = asArray(await getJson(`${catalogServiceUrl}/v1/categories`)).filter(isValidCatalogRef);
  let collections = asArray(await getJson(`${catalogServiceUrl}/v1/collections`)).filter(isValidCatalogRef);

  // Fallback for broken query-side projections: rely on POST responses from current run.
  const hasRequiredRefs = brands.length > 0 && categories.length > 0 && collections.length > 0;
  if (!hasRequiredRefs) {
    const brandBySlug = new Map(brands.map((x) => [x.slug, x]));
    const categoryBySlug = new Map(categories.map((x) => [x.slug, x]));
    const collectionBySlug = new Map(collections.map((x) => [x.slug, x]));

    for (const brand of seedBrands) {
      if (!brandBySlug.has(brand.slug)) {
        const created = await postJson(`${catalogServiceUrl}/v1/brands`, brand);
        if (isValidCatalogRef(created)) {
          brandBySlug.set(created.slug, created);
        }
      }
    }

    for (const category of seedCategories) {
      if (!categoryBySlug.has(category.slug)) {
        const created = await postJson(`${catalogServiceUrl}/v1/categories`, category);
        if (isValidCatalogRef(created)) {
          categoryBySlug.set(created.slug, created);
        }
      }
    }

    for (const collection of seedCollections) {
      if (!collectionBySlug.has(collection.slug)) {
        const created = await postJson(`${catalogServiceUrl}/v1/collections`, collection);
        if (isValidCatalogRef(created)) {
          collectionBySlug.set(created.slug, created);
        }
      }
    }

    brands = Array.from(brandBySlug.values());
    categories = Array.from(categoryBySlug.values());
    collections = Array.from(collectionBySlug.values());
  }

  if (brands.length === 0 || categories.length === 0 || collections.length === 0) {
    throw new Error('Missing valid catalog references after seed (brands/categories/collections).');
  }

  console.log(`[seed] creating ${seedCount} products via CRUD endpoints`);
  for (let i = 0; i < seedCount; i += 1) {
    await postJsonWithRetry(
      `${catalogServiceUrl}/v1/products`,
      randomProduct(i + 1, brands, categories, collections),
      {
        retries: 8,
        delayMs: 300,
        shouldRetry: (message) => message.includes('Invalid product references')
      });
  }

  const products = await getJson(`${catalogServiceUrl}/v1/products`);
  const newArrivals = await getJson(`${catalogServiceUrl}/v1/products/new-arrivals`);
  const bestSellers = await getJson(`${catalogServiceUrl}/v1/products/best-sellers`);

  console.log('[seed] upserting warehouse stock');
  for (const product of Array.isArray(products) ? products : []) {
    if (!product?.id || !product?.sku) {
      continue;
    }

    await postJson(`${warehouseServiceUrl}/v1/stock`, {
      productId: product.id,
      sku: product.sku,
      availableQuantity: randomInt(15, 60)
    });
  }

  console.log(`[seed] done, catalog size: ${Array.isArray(products) ? products.length : 0}`);
  console.log(`[seed] new arrivals: ${Array.isArray(newArrivals) ? newArrivals.length : 0}`);
  console.log(`[seed] best sellers: ${Array.isArray(bestSellers) ? bestSellers.length : 0}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
